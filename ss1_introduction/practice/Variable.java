package ss1_introduction.practice;

public class Variable {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        double d = 30.5;
        boolean b = true;
        char c = 'A';
        String s = "Hà nội";
        System.out.println("i = " + i + " f = " + f + " d = " + d + " b = " + b + " c = " + c + " s = " + s);
    }

}
